<?php

namespace App\Repository;

use App\Entity\Demande;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Demande>
 *
 * @method Demande|null find($id, $lockMode = null, $lockVersion = null)
 * @method Demande|null findOneBy(array $criteria, array $orderBy = null)
 * @method Demande[]    findAll()
 * @method Demande[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class DemandeRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Demande::class);
    }

    public function add(Demande $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Demande $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function searchByWord($search){

        // Création du query builder qui permet de faire des requêtes SQL en PHP
        $qb = $this->createQueryBuilder('demande');
        // SELECT de la table Demande
        $query = $qb->select('demande')
            // Recupération des ldemandes où le titre est 'search'
            ->where('demande.title LIKE :search')
            // Paramétrage de 'search' où il sera trouvé malgré présence, avant ou après,
            // des caractères de la chaine de caractères $search
            ->setParameter('search', '%'.$search.'%')
            // Récupération de la requête
            ->getQuery();
        //renvoie en base de données
        return $query->getResult();
    }

